package com.capgmini.helloworld;


public class DoWhileLoop {
	
	public static void main(String args[])
	{
		//For is entry control loop
		//While is also control loop
		
		//DO while //exit control loop
		
		//do block
		int i=0;
		do
		{
		System.out.println(i);
		
		//Increment/ decrement 	
		i++;	
		}
//		while(true);
		while(i<8);
	}

}
