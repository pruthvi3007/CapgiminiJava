package com.capgi.day5;
//it  converts from one data type to another
public class TypeCasting {
	public static void main(String arg[])
	{
		float f=1.0f;
		int a= (int)f;
		
		double d= 1.0;
		int b=(int)d;
		
		double dd=(double)f;
		System.out.println(a);
		System.out.println(b);
		System.out.println(dd);
	}


}
